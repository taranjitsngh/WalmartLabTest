import Foundation
import UIKit

// *********************************************************************************************
// # MARK: - Extension

extension UIColor {
    
    public class var inStockGreenColor: UIColor {
        return #colorLiteral(red: 0, green: 0.5603182912, blue: 0, alpha: 1)
    }
    
    public class var outOfStockRedColor: UIColor {
        return #colorLiteral(red: 1, green: 0.08731482584, blue: 0.2058391994, alpha: 1)
    }

}
